from tree_search import *

class DigDug(SearchDomain):
    def __init__(self, enemies, rocks, map):
        self.enemies = enemies
        self.rocks = rocks
        self.map = map
    
    def can_go(self, x, y):
        for rock in self.rocks:
            if rock["pos"][0] == x and rock["pos"][1] == y:
                return False
            
        for enemy in self.enemies:
            if enemy["pos"][0] == x and enemy["pos"][1] == y:
                return False
            
        if self.map["size"][0] < x or x < 0:
            return False
        if self.map["size"][1] < y or y < 0:
            return False
        
        return True

    def actions(self, state):
        actlist = []
        x, y = state["digdug"]
        if self.can_go(x, y-1):
            actlist.append("w")
        if self.can_go(x-1, y):
            actlist.append("a")
        if self.can_go(x, y+1):
            actlist.append("s")
        if self.can_go(x+1, y):
            actlist.append("d")
        return actlist
    
    def result(self, state, action):
        newstate = state.copy()
        x, y = state["digdug"]
        if action == "w":
            newstate["digdug"] = [x, y-1]
        elif action == "a":
            newstate["digdug"] = [x-1, y]
        elif action == "s":
            newstate["digdug"] = [x, y+1]
        elif action == "d":
            newstate["digdug"] = [x+1, y]
        return newstate
    
    def cost(self, state, action):
        return 1
    
    def heuristic(self, state, goal):
        return abs(state["digdug"][0] - goal["digdug"][0]) + abs(state["digdug"][1] - goal["digdug"][1])
    
    def satisfies(self, state, goal):
        return state["digdug"] == goal["digdug"]
    

